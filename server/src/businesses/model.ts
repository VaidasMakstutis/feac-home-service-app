import mongoose from 'mongoose';
import { Business } from './types';

const businessSchema = new mongoose.Schema<Business>({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  contactPerson: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

export const BusinessModel = mongoose.model<Business>('Business', businessSchema);
